champion_class(X,Y) :- 
	champion(X), 
    class(Y), 
    has_playstyle(X,Z), 
    class_playstyle(Y,A), 
    member(Z,A).

select_cost(X,Y) :- 
    champion(X), 
    skill_cost(Y),
    use_skill_cost(X,Y).

select_class(X, Y) :- 
    class(X), 
    champion(Y), 
    has_playstyle(Y,Z), 
    class_playstyle(X,A), 
    member(Z,A).

compatible_champ(X,Y) :- 
    champion(X), 
    champion(Y), 
    has_playstyle(X,A), 
    has_playstyle(Y,B), 
    compatible(A, C), 
    member(B, C).

beginner_select_class(X,Y) :- 
    class(X), 
    champion(Y), 
    champion_class(Y,X), 
    has_difficulty(Y,1).

beginner_select_style(X,Y) :- 
    playstyle(X), 
    champion(Y), 
    has_playstyle(Y,X), 
    has_difficulty(Y,1).

solo_counter_pick(X,Y) :- 
    champion(X), 
    champion(Y), 
    champion_class(X,A), 
    champion_class(Y,B), 
    has_advantage(A, C),
    member(B,C).

duo_counter_pick(X, Y) :-
    champion(X),
    member(A, Y),
    champion(A),
    solo_counter_pick(X,A).

counter_same_role(X,Y) :- 
    champion(X), 
    champion(Y), 
    solo_counter_pick(X,Y), 
    has_role(X,A), 
    has_role(Y,B), 
    A==B.

select_based_on_pick(X,Y,Z) :- 
    champion(X), 
    member(A, Y), 
    champion(A), 
    compatible_champ(X,A), 
    member(B, Z), 
    champion(B), 
    solo_counter_pick(X,B).