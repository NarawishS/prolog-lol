champion_class_style(X, Y, Z) :- 
    champion(X),
    class(Y),
    playstyle(Z),
    has_class(X,Y),
    has_playstyle(X,Z).

champion_cost_role(X, Y, Z) :-
    champion(X),
    skill_cost(Y),
    role(Z),
    use_skill_cost(X,Y),
    has_role(X,A),
    member(Z,A).


champion_class_role(X, Y, Z) :-
    champion(X),
    class(Y),
    role(Z),
    has_class(X,Y),
    has_role(X,A),
    member(Z,A).

champion_style_role(X, Y, Z) :-
    champion(X),
    playstyle(Y),
    role(Z),
    has_playstyle(X,Y),
    has_role(X,A),
    member(Z,A).

champion_specific(X,A,B,C,D) :-
    champion(X),
    class(A),
    playstyle(B),
    skill_cost(C),
    role(D),
    champion_class_style(X,A,B),
    champion_cost_role(X,C,D).

compatible_champ(X,Y) :-
    champion(X),
    champion(Y),
    has_playstyle(X,A),
    has_playstyle(Y,B),
    compatible(A, C),
    member(B, C).

beginner_select_class(X,Y) :-
    class(X), champion(Y),
    has_class(Y,X),
    has_difficulty(Y,1).

beginner_select_style(X,Y) :-
    playstyle(X),
    champion(Y),
    has_playstyle(Y,X),
    has_difficulty(Y,1).

solo_counter_pick(X,Y) :-
    champion(X),
    champion(Y),
    has_class(X,A),
    has_class(Y,B),
    has_advantage(A, C),
    member(B, C).

duo_counter_pick(X, Y) :-
    champion(X),
    member(A, Y),
    champion(A),
    solo_counter_pick(X,A).

counter_same_role(X,Y) :-
    champion(X),
    champion(Y),
    solo_counter_pick(X,Y),
    has_role(X,A),
    member(C,A),
    has_role(Y,B),
    member(D,B),
    C==D.

meta_by_style(X,Y) :-
    role(X),
    champion(Y),
    has_role(Y, A),
    member(X, A),
    meta(X, B),
    member(Y, B). 

select_based_on_pick(X,Y,Z) :-
    champion(X),
    member(A, Y),
    champion(A),
    compatible_champ(X,A),
    member(B, Z),
    champion(B),
    solo_counter_pick(X,B).